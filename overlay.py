def load_progress():
    if os.path.exists(SAVE_FILE):
        with open(SAVE_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}


def save_progress(progress):
    with open(SAVE_FILE, "w", encoding="utf-8") as f:
        json.dump(progress, f, ensure_ascii=False)


class OverlayApp:
    def __init__(self, root):
        self.root = root
        self.root.title("PW Координаты")
        self.root.attributes("-topmost", True)
        self.root.attributes("-alpha", 0.92)
        self.root.configure(bg="#1a1a2e")
        self.root.geometry("320x540+50+50")
        self.root.resizable(False, False)

        self.progress = load_progress()
        self.current_map = 0
        self.current_loc = 0
        self.current_page = 0
        self.PAGE_SIZE = 12
        self.current_tab = 0  # 0=сундуки 1=отблески
        self.check_vars = []

        self._build_ui()
        self._render_location()

    def _build_ui(self):
        # Шапка с картами
        top = tk.Frame(self.root, bg="#1a1a2e")
        top.pack(fill="x", padx=6, pady=(6,0))

        self.map_btns = []
        for i, name in enumerate(MAP_NAMES):
            short = name.replace("Город ", "")
            b = tk.Button(top, text=short, font=("Arial", 10),
                         bg="#2d2d4e", fg="#aaaacc", relief="flat",
                         activebackground="#3d3d6e", activeforeground="white",
                         cursor="hand2", padx=8, pady=3,
                         command=lambda i=i: self._set_map(i))
            b.pack(side="left", padx=2)
            self.map_btns.append(b)

        # Вкладки: Сундуки / Отблески
        tabs = tk.Frame(self.root, bg="#1a1a2e")
        tabs.pack(fill="x", padx=6, pady=(4,0))
        self.tab_btns = []
        for i, label in enumerate(["Сундуки", "Отблески"]):
            b = tk.Button(tabs, text=label, font=("Arial", 9),
                         bg="#4a3a80" if i==0 else "#2d2d4e",
                         fg="white" if i==0 else "#aaaacc",
                         relief="flat", cursor="hand2", padx=8, pady=2,
                         command=lambda i=i: self._set_tab(i))
            b.pack(side="left", padx=2)
            self.tab_btns.append(b)

        # Название локации
        self.loc_label = tk.Label(self.root, text="", font=("Arial", 11, "bold"),
                                   bg="#1a1a2e", fg="#e0d0ff", wraplength=300)
        self.loc_label.pack(fill="x", padx=8, pady=(8,2))

        # Прогресс локации
        self.prog_label = tk.Label(self.root, text="", font=("Arial", 9),
                                    bg="#1a1a2e", fg="#888899")
        self.prog_label.pack(anchor="w", padx=10)

        # Скролл с точками
        frame = tk.Frame(self.root, bg="#1a1a2e")
        frame.pack(fill="both", expand=True, padx=6, pady=4)

        canvas = tk.Canvas(frame, bg="#1a1a2e", highlightthickness=0)
        scrollbar = ttk.Scrollbar(frame, orient="vertical", command=canvas.yview)
        self.scroll_frame = tk.Frame(canvas, bg="#1a1a2e")

        def on_frame_configure(e):
            canvas.configure(scrollregion=canvas.bbox("all"))
        def on_canvas_configure(e):
            canvas.itemconfig(canvas_window, width=e.width)
        self.scroll_frame.bind("<Configure>", on_frame_configure)
        canvas_window = canvas.create_window((0, 0), window=self.scroll_frame, anchor="nw")
        canvas.bind("<Configure>", on_canvas_configure)
        canvas.configure(yscrollcommand=scrollbar.set)
        canvas.bind("<MouseWheel>", lambda e: canvas.yview_scroll(-1*(e.delta//120), "units"))
        self.scroll_frame.bind("<MouseWheel>", lambda e: canvas.yview_scroll(-1*(e.delta//120), "units"))

        canvas.pack(side="left", fill="both", expand=True)
        scrollbar.pack(side="right", fill="y")

        # Нижняя панель локаций
        bot = tk.Frame(self.root, bg="#1a1a2e")
        bot.pack(fill="x", padx=6, pady=(0,6))
        self.bot_frame = bot

        # Навигация по страницам (над нижней панелью)
        self.pnav_frame = tk.Frame(self.root, bg="#1a1a2e")
        self.page_prev_btn = tk.Button(self.pnav_frame, text="◀", font=("Arial", 9),
                  bg="#2d2d4e", fg="#aaaacc", relief="flat",
                  cursor="hand2", padx=8, pady=2,
                  command=self._prev_page)
        self.page_prev_btn.pack(side="left")
        self.page_lbl = tk.Label(self.pnav_frame, text="",
                     font=("Arial", 9), bg="#1a1a2e", fg="#666688")
        self.page_lbl.pack(side="left")
        self.page_next_btn = tk.Button(self.pnav_frame, text="▶", font=("Arial", 9),
                  bg="#2d2d4e", fg="#aaaacc", relief="flat",
                  cursor="hand2", padx=8, pady=2,
                  command=self._next_page)
        self.page_next_btn.pack(side="left")

        self.prev_btn = tk.Button(bot, text="◀ Назад", font=("Arial", 10),
                                   bg="#2d2d4e", fg="#aaaacc", relief="flat",
                                   activebackground="#3d3d6e", activeforeground="white",
                                   cursor="hand2", padx=10, pady=4,
                                   command=self._prev_loc)
        self.prev_btn.pack(side="left")

        self.nav_label = tk.Label(bot, text="", font=("Arial", 9),
                                   bg="#1a1a2e", fg="#666688")
        self.nav_label.pack(side="left", expand=True)

        self.next_btn = tk.Button(bot, text="Далее ▶", font=("Arial", 10),
                                   bg="#5a3fa0", fg="white", relief="flat",
                                   activebackground="#7a5fc0", activeforeground="white",
                                   cursor="hand2", padx=10, pady=4,
                                   command=self._next_loc)
        self.next_btn.pack(side="right")

    def _set_tab(self, i):
        self.current_tab = i
        self.current_loc = 0
        self.current_page = 0
        for j, b in enumerate(self.tab_btns):
            b.configure(bg="#4a3a80" if j==i else "#2d2d4e",
                        fg="white" if j==i else "#aaaacc")
        self._render_location()

    def _set_map(self, i):
        self.current_map = i
        self.current_loc = 0
        self.current_page = 0
        for j, b in enumerate(self.map_btns):
            b.configure(bg="#4a3a80" if j == i else "#2d2d4e",
                        fg="white" if j == i else "#aaaacc")
        self._render_location()

    def _render_location(self):
        map_name = MAP_NAMES[self.current_map]
        locs = REFLECTIONS[map_name] if self.current_tab == 1 else MAPS[map_name]
        loc = locs[self.current_loc]
        points = loc["points"]
        total_pages = max(1, (len(points) + self.PAGE_SIZE - 1) // self.PAGE_SIZE)
        if self.current_page >= total_pages:
            self.current_page = 0

        self.loc_label.config(text=loc["name"])
        self.nav_label.config(text=f"{self.current_loc+1} / {len(locs)}")

        # Очистить старые виджеты
        for w in self.scroll_frame.winfo_children():
            w.destroy()
        self.check_vars = []

        # Страница точек
        start = self.current_page * self.PAGE_SIZE
        end = min(start + self.PAGE_SIZE, len(points))
        page_points = points[start:end]

        done = sum(1 for i in range(len(points)) if self.progress.get(f"{map_name}|{loc['name']}|{i}", False))

        for idx, (x, y, z, task) in enumerate(page_points):
            i = start + idx
            pk = f"{map_name}|{loc['name']}|{i}"
            pk_copied = pk + "|copied"
            var = tk.BooleanVar(value=self.progress.get(pk, False))
            var_copied = tk.BooleanVar(value=self.progress.get(pk_copied, False))
            self.check_vars.append((pk, var))

            row = tk.Frame(self.scroll_frame, bg="#1a1a2e")
            row.pack(fill="x", pady=0, padx=4)

            cb_copied = tk.Checkbutton(row, variable=var_copied, bg="#1a1a2e",
                                activebackground="#1a1a2e",
                                selectcolor="#1a4a3a",
                                command=lambda pk=pk_copied, v=var_copied: self._toggle(pk, v))
            cb_copied.pack(side="left")

            cb = tk.Checkbutton(row, variable=var, bg="#1a1a2e",
                                activebackground="#1a1a2e",
                                selectcolor="#3d3d6e",
                                command=lambda pk=pk, v=var: self._toggle(pk, v))
            cb.pack(side="left")

            coord = f"{x} {y}"
            coord_display = coord + (f" ({z})" if z is not None else "")
            coord_btn = tk.Button(row, text=coord_display,
                                  font=("Courier", 10, "bold"),
                                  bg="#2a2a44", fg="#c8b8ff",
                                  relief="flat", cursor="hand2",
                                  activebackground="#3a3a64",
                                  activeforeground="white",
                                  padx=4, pady=1,
                                  command=lambda c=coord, pk=pk_copied, v=var_copied: self._copy_and_check(c, pk, v))
            coord_btn.pack(side="left", padx=(4,4))

            tk.Label(row, text=task, font=("Arial", 8),
                     bg="#1a1a2e", fg="#888899").pack(side="left")



        self.prog_label.config(text=f"Выполнено: {done} / {len(points)}")

        # Обновить навигацию по страницам
        if total_pages > 1:
            self.pnav_frame.pack(fill="x", padx=6, pady=(0,2), before=self.bot_frame)
            self.page_prev_btn.config(state="normal" if self.current_page > 0 else "disabled")
            self.page_lbl.config(text=f"  {self.current_page+1} / {total_pages}  ")
            self.page_next_btn.config(state="normal" if self.current_page < total_pages-1 else "disabled")
        else:
            self.pnav_frame.pack_forget()
        self.prev_btn.config(state="normal" if self.current_loc > 0 else "disabled")
        self.next_btn.config(text="Далее ▶" if self.current_loc < len(locs)-1 else "✓ Конец")

    def _toggle(self, pk, var):
        self.progress[pk] = var.get()
        save_progress(self.progress)
        # Обновить счётчик
        map_name = MAP_NAMES[self.current_map]
        loc = MAPS[map_name][self.current_loc]
        done = sum(1 for k, v in self.check_vars if v.get())
        self.prog_label.config(text=f"Выполнено: {done} / {len(loc['points'])}")

    def _copy_and_check(self, text, pk, var):
        var.set(True)
        self.progress[pk] = True
        save_progress(self.progress)
        self._copy(text)

    def _copy(self, text):
        self.root.clipboard_clear()
        self.root.clipboard_append(text)
        self.root.update()
        # Мигнуть подтверждение
        old = self.loc_label.cget("fg")
        self.loc_label.config(fg="#00cc88", text="✓ Скопировано!")
        self.root.after(800, lambda: self.loc_label.config(
            fg=old, text=MAPS[MAP_NAMES[self.current_map]][self.current_loc]["name"]))

    def _next_page(self):
        self.current_page += 1
        self._render_location()

    def _prev_page(self):
        self.current_page -= 1
        self._render_location()

    def _next_loc(self):
        map_name = MAP_NAMES[self.current_map]
        locs = MAPS[map_name]
        if self.current_loc < len(locs) - 1:
            self.current_loc += 1
            self.current_page = 0
            self._render_location()

    def _prev_loc(self):
        if self.current_loc > 0:
            self.current_loc -= 1
            self.current_page = 0
            self._render_location()


if __name__ == "__main__":
    root = tk.Tk()
